    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        /*variaveis para recever da tela */
        String nome,senha,telefone,email;
        String matricula;
        nome=jTextNome.getText();
        senha=jTextSenha.getText();
        telefone=jTextTelefone.getText();
        email=jTextEmail.getText();
        matricula=jTextMatricula.getText();
                
        
        /* criando o objeto Professor*/
        Professor objProfessor= new Professor();
        objProfessor.setNomeProfessor(nome);
        objProfessor.setSenhaProfessor(senha);
        objProfessor.setTelefone(telefone);
        objProfessor.setEmail(email);
        objProfessor.setMatriculaProf(matricula);
        DAOProfessor objdao= new DAOProfessor();
        objdao.salvarProfessor(objProfessor);
        montartabelaProf();
           jTextNome.setText(null);
            jTextSenha.setText(null);
            jTextTelefone.setText(null);
            jTextEmail.setText(null);
            jTextMatricula.setText(null);
          
       
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

