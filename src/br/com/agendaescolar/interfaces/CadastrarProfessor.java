    private void montartabelaProf(){
           String colunas[] = new String[]{"Matricula","Nome","Telefone","Email"};
           DefaultTableModel modelotabela = new DefaultTableModel(null,colunas);
           DAOProfessor  prof= new DAOProfessor();
           ArrayList<Professor> lista= prof.listarProfessor();
           
           for(Professor meuprof: lista){
                modelotabela.addRow(new Object[]{meuprof.getMatriculaProf(),meuprof.getNomeProfessor(),meuprof.getTelefone(),meuprof.getEmail()});
           }
        jTableProfessor.setModel(modelotabela);
    }

