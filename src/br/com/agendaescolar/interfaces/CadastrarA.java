      private void montartabelaAluno(){
           String colunas[] = new String[]{"Id","Nome","matricula"};
           DefaultTableModel modelotabela = new DefaultTableModel(null,colunas);
           DAOAluno ALUNO= new DAOAluno();
           ArrayList<Aluno> lista= ALUNO.listarAluno();
           
           for(Aluno meualuno: lista){
                modelotabela.addRow(new Object[]{meualuno.getCod(), meualuno.getNomeAluno(),meualuno.getMatricula()});
           }
        jTableaAlunos.setModel(modelotabela);
    }
    

