    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
     
      int idaluno;
     String valorid;
     boolean resposta;
     valorid=jTextIdAluno.getText();
     if(valorid.isEmpty()){
         JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela");
     }
     else {
         idaluno=  Integer.parseInt(valorid);
         DAOAluno objdao= new DAOAluno();
         resposta=objdao.apagar(idaluno);
         if(resposta==true){
             JOptionPane.showMessageDialog(null, "Exclusão realizada com sucessor");
             montartabelaAluno();
         }
         else{
             JOptionPane.showMessageDialog(null, "Seu aluno esta vinculado em outras tabelas");
         }
     }
    
     
     
    }//GEN-LAST:event_jButtonExcluirActionPerformed

